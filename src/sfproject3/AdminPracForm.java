            pstmt.setString(1, Title);
            pstmt.setString(2, Des);
            pstmt.setString(3, ModCode);
            pstmt.setString(4, StartPracDate);
            pstmt.setString(5, EndPracDate);
            pstmt.setBoolean(6,AvailableChecking);
